CREATE TABLE IF NOT EXISTS t_compagnie (
	id INT(11) NOT NULL AUTO_INCREMENT,
	raisonSociale VARCHAR(100) DEFAULT NULL,
	raisonSocialeAbrege VARCHAR(50) DEFAULT NULL,
	codeIntermediaire INT(12) DEFAULT NULL,
	codeMF INT(12) DEFAULT NULL,
	correspondantAuto VARCHAR(50) DEFAULT NULL,
	telCorrespondantAuto VARCHAR(50) DEFAULT NULL,
	correspondantSinistre VARCHAR(50) DEFAULT NULL,
	telCorrespondantSinistre VARCHAR(50) DEFAULT NULL,
	correspondantRecouvrement VARCHAR(50) DEFAULT NULL,
	telCorrespondantRecouvrement VARCHAR(50) DEFAULT NULL,
	adresse VARCHAR(100) DEFAULT NULL,
	rue VARCHAR(50) DEFAULT NULL,
	fax VARCHAR(50) DEFAULT NULL,
	tel VARCHAR(50) DEFAULT NULL,
	email VARCHAR(100) DEFAULT NULL,
	created DATETIME DEFAULT NULL,
	createdBy VARCHAR(50) DEFAULT NULL,
	updated DATETIME DEFAULT NULL,
	updatedBy VARCHAR(50) DEFAULT NULL,
