CREATE TABLE IF NOT EXISTS t_incendie (
	id INT(11) NOT NULL AUTO_INCREMENT,
	codeCompagnie INT(12) DEFAULT NULL,
	codeUsage VARCHAR(50) DEFAULT NULL,
	codeClasse VARCHAR(50) DEFAULT NULL,
	codeSousClasse INT(12) DEFAULT NULL,
	formuleIncendie INT(12) DEFAULT NULL,
	tauxMille DECIMAL(12,2) DEFAULT NULL,
	tauxCommission DECIMAL(12,2) DEFAULT NULL,
	tauxTPS DECIMAL(12,2) DEFAULT NULL,
	tauxTaxe DECIMAL(12,2) DEFAULT NULL,
	designation VARCHAR(255) DEFAULT NULL,
	montantFranchise DECIMAL(12,2) DEFAULT NULL,
	tauxFranchise DECIMAL(12,2) DEFAULT NULL,
	montant DECIMAL(12,2) DEFAULT NULL,
	formule INT(12) DEFAULT NULL,
	observation VARCHAR(255) DEFAULT NULL,
	created DATETIME DEFAULT NULL,
	createdBy VARCHAR(50) DEFAULT NULL,
	updated DATETIME DEFAULT NULL,
	updatedBy VARCHAR(50) DEFAULT NULL,
	PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;