CREATE TABLE IF NOT EXISTS t_tarifrc (
	id INT(11) NOT NULL AUTO_INCREMENT,
	codeCompagnie INT(12) DEFAULT NULL,
	codeUsage VARCHAR(5) DEFAULT NULL,
	codeClasse INT(12) DEFAULT NULL,
	codeSousClasse INT(12) DEFAULT NULL,
	carburant VARCHAR(20) DEFAULT NULL,
	puissanceFiscale INT(12) DEFAULT NULL,
	primeRC DECIMAL(12,2) DEFAULT NULL,
	majorationRemorque DECIMAL(12,2) DEFAULT NULL,
	matiereInflamable DECIMAL(12,2) DEFAULT NULL,
	transportPersonne DECIMAL(12,2) DEFAULT NULL,
	tauxCommission DECIMAL(12,2) DEFAULT NULL,
	tauxTPS DECIMAL(12,2) DEFAULT NULL,
	tauxTaxe DECIMAL(12,2) DEFAULT NULL,
	timbre DECIMAL(12,2) DEFAULT NULL,
	created DATETIME DEFAULT NULL,
	createdBy VARCHAR(50) DEFAULT NULL,
	updated DATETIME DEFAULT NULL,
	updatedBy VARCHAR(50) DEFAULT NULL,
	PRIMARY KEY (id)
